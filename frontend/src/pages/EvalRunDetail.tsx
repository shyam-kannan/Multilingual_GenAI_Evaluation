import { useEffect, useState } from "react";
import { useParams, Link } from "react-router-dom";
import { endpoints } from "../api/client";
import type { EvalRun } from "../types";
import EvalResultCard from "../components/EvalResultCard";
import LocaleBadge from "../components/LocaleBadge";

export default function EvalRunDetail() {
  const { id } = useParams<{ id: string }>();
  const [run, setRun] = useState<EvalRun | null>(null);
  const [error, setError] = useState("");

  useEffect(() => {
    if (!id) return;
    endpoints
      .getEvalRun(id)
      .then(setRun)
      .catch(() => setError("Failed to load eval run"));
  }, [id]);

  if (error) return <p className="text-red-600 py-8">{error}</p>;
  if (!run) return <p className="text-gray-500 py-8">Loading...</p>;

  return (
    <div>
      <Link
        to="/"
        className="text-sm text-indigo-600 hover:text-indigo-900 mb-4 inline-block"
      >
        &larr; Back to Overview
      </Link>

      <div className="flex items-center gap-4 mb-2">
        <h2 className="text-2xl font-bold text-gray-900">
          Evaluation Run Detail
        </h2>
        <LocaleBadge locale={run.locale} passRate={-1} />
        <span
          className={`px-3 py-1 rounded text-sm font-medium ${
            run.overall_passed
              ? "bg-green-100 text-green-800"
              : "bg-red-100 text-red-800"
          }`}
        >
          {run.overall_passed ? "OVERALL PASS" : "OVERALL FAIL"}
        </span>
      </div>

      <p className="text-sm text-gray-500 mb-2">
        ID: {run.id} &middot;{" "}
        {new Date(run.created_at).toLocaleString()}
      </p>
      <p className="text-sm text-gray-500 mb-6 max-w-3xl">
        This page shows the full evaluation of a single AI-generated response.
        The input was sent to the AI model, and the output was then scored by
        four independent checks. All four must pass for the overall result to be
        "PASS."
      </p>

      {/* Input/Output */}
      <div className="grid grid-cols-1 md:grid-cols-2 gap-6 mb-8">
        <div className="bg-white rounded-lg shadow p-6">
          <h3 className="text-sm font-medium text-gray-500 mb-1">Input</h3>
          <p className="text-xs text-gray-400 mb-2">
            The user's question or request that was sent to the AI
          </p>
          <p className="text-gray-900 whitespace-pre-wrap" dir="auto">
            {run.input_text}
          </p>
        </div>
        <div className="bg-white rounded-lg shadow p-6">
          <h3 className="text-sm font-medium text-gray-500 mb-1">
            AI Output
          </h3>
          <p className="text-xs text-gray-400 mb-2">
            The response generated by the language model
          </p>
          <p className="text-gray-900 whitespace-pre-wrap" dir="auto">
            {run.llm_output}
          </p>
        </div>
      </div>

      {/* Score cards */}
      <h3 className="text-lg font-medium text-gray-900 mb-1">
        Evaluation Results
      </h3>
      <p className="text-sm text-gray-500 mb-4">
        Each check below was performed by a separate AI judge or rule-based
        validator. Click "View raw details" for the full machine-readable
        output.
      </p>
      <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
        <EvalResultCard
          title="Quality"
          description="Measures relevance, completeness, and coherence. Scored 0-100%, must be 70%+ to pass."
          passed={run.quality_score >= 0.7}
          score={run.quality_score}
          reasoning={run.quality_reasoning}
        />
        <EvalResultCard
          title="Hallucination"
          description="Detects fabricated or unsupported claims. Scored 0-100%, must be 30% or lower to pass."
          passed={run.hallucination_score <= 0.3}
          score={run.hallucination_score}
          reasoning={run.hallucination_reasoning}
        />
        <EvalResultCard
          title="Moderation"
          description="Checks for harmful, unsafe, or inappropriate content. Binary pass/fail. Errors default to fail (fail-closed)."
          passed={run.moderation_passed}
          reasoning={run.moderation_reasoning}
        />
        <EvalResultCard
          title="World-Readiness"
          description="Validates correct script, text direction, and formatting for the target language and culture."
          passed={run.world_readiness_passed}
          reasoning={
            run.world_readiness_details?.checks
              ? (
                  run.world_readiness_details.checks as {
                    check: string;
                    passed: boolean;
                    detail: string;
                  }[]
                )
                  .map(
                    (c: {
                      check: string;
                      passed: boolean;
                      detail: string;
                    }) =>
                      `${c.passed ? "PASS" : "FAIL"} ${c.check}: ${c.detail}`
                  )
                  .join("\n")
              : "No details available"
          }
          details={run.world_readiness_details}
        />
      </div>
    </div>
  );
}
